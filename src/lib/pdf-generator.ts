// SMM PLAN PDF
// ============================================================
export function generateSMMPlanPDF(data: {
  overview: string;
  contentMix: any;
  pillars: any[];
  calendar: any[];
  kpiTargets: any;
  duration: string;
}) {
  let html = `
    <div class="header">
      <h1>Social Media Content Plan</h1>
      <p>${data.duration} | Neelachala Homes</p>
    </div>
  `;

  html += '<h1>Overview</h1>';
  html += '<div class="section"><p>' + data.overview + '</p></div>';

  if (data.contentMix) {
    html += '<h2>Content Mix</h2>';
    html += '<div class="kpi-grid">';
    Object.entries(data.contentMix).forEach(([type, count]) => {
      html += '<div class="kpi-card"><div class="value">' + count + '</div><div class="label">' + type.replace(/([A-Z])/g, ' $1') + '</div></div>';
    });
    html += '</div>';
  }

  if (data.pillars) {
    html += '<h2>Content Pillars</h2>';
    data.pillars.forEach((p: any) => {
      html += '<div class="field-row"><span class="field-label"><strong>' + p.pillar + '</strong> (' + p.freq + ')</span><span class="field-value">' + p.purpose + '</span></div>';
    });
  }

  if (data.kpiTargets) {
    html += '<h2>KPI Targets</h2>';
    Object.entries(data.kpiTargets).forEach(([k, v]) => {
      html += '<div class="field-row"><span class="field-label">' + k.replace(/([A-Z])/g, ' $1') + '</span><span class="field-value">' + v + '</span></div>';
    });
  }

  if (data.calendar && data.calendar.length > 0) {
    html += '<div class="page-break"></div><h1>Content Calendar</h1>';
    data.calendar.forEach((post: any) => {
      html += '<div class="calendar-row">';
      html += '<div class="cal-date">' + post.date + '<br/><small>' + (post.day || '') + '</small></div>';
      html += '<div class="cal-type">' + (post.type || '') + '</div>';
      html += '<div class="cal-topic"><strong>' + (post.topic || '') + '</strong>';
      if (post.time) html += '<br/><small>Post at ' + post.time + ' on ' + (post.platform || 'both') + '</small>';
      html += '</div></div>';
    });

    // Detailed posts
    html += '<div class="page-break"></div><h1>Post Details</h1>';
    data.calendar.forEach((post: any, i: number) => {
      html += '<h2>' + post.date + ' — ' + (post.topic || 'Post ' + (i + 1)) + '</h2>';
      html += '<div class="field-row"><span class="field-label">Type</span><span class="field-value">' + (post.type || '') + ' on ' + (post.platform || 'both') + '</span></div>';
      html += '<div class="field-row"><span class="field-label">Time</span><span class="field-value">' + (post.time || '') + '</span></div>';
      if (post.captionEn) html += '<div class="section"><strong>Caption:</strong><br/>' + post.captionEn + '</div>';
      if (post.hashtags) html += '<p>' + post.hashtags.map((h: string) => '<span class="tag">#' + h + '</span>').join('') + '</p>';
      if (post.nanoPrompt) html += '<div class="prompt-box">' + post.nanoPrompt + '</div>';
      if (post.reelScript) html += '<div class="section"><strong>Reel Script:</strong><br/>' + post.reelScript + '</div>';
    });
  }

  downloadPDF(html, 'NH_SMM_Plan_' + data.duration.replace(/\s/g, '_'));
}

