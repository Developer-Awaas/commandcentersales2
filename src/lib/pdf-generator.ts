// SMM ANALYZER REPORT PDF
// ============================================================
export function generateSMMReportPDF(data: {
  platform: string;
  period: string;
  analysis: any;
  metrics: any;
}) {
  let html = `
    <div class="header">
      <h1>Social Media Performance Report</h1>
      <p>${data.platform} | ${data.period} | Neelachala Homes</p>
    </div>
  `;

  if (data.analysis?.healthScore) {
    const sc = data.analysis.healthScore >= 7 ? 'green' : data.analysis.healthScore >= 4 ? 'yellow' : 'red';
    html += '<h1>Overall Health</h1>';
    html += '<p>Score: <span class="score-badge score-' + sc + '">' + data.analysis.healthScore + '/10</span></p>';
    html += '<div class="section"><p>' + (data.analysis.assessment || '') + '</p></div>';
  }

  if (data.analysis?.scorecard) {
    html += '<h2>Metrics Scorecard</h2>';
    data.analysis.scorecard.forEach((s: any) => {
      html += '<div class="field-row"><span class="field-label">' + s.metric + '</span><span class="field-value">' + s.value + ' (benchmark: ' + s.benchmark + ') [' + s.status.toUpperCase() + ']</span></div>';
    });
  }

  if (data.analysis?.contentPerformance) {
    html += '<h2>Content Performance</h2>';
    html += '<div class="section">';
    html += '<p><strong>Best:</strong> ' + data.analysis.contentPerformance.bestType + '</p>';
    html += '<p><strong>Worst:</strong> ' + data.analysis.contentPerformance.worstType + '</p>';
    html += '<p><strong>Recommendation:</strong> ' + data.analysis.contentPerformance.recommendation + '</p>';
    html += '</div>';
  }

  if (data.analysis?.timingAnalysis) {
    html += '<h2>Timing Analysis</h2>';
    html += '<div class="section">';
    html += '<p><strong>Best Day:</strong> ' + data.analysis.timingAnalysis.bestDay + ' at ' + data.analysis.timingAnalysis.bestTime + '</p>';
    html += '<p><strong>Recommendation:</strong> ' + data.analysis.timingAnalysis.recommendation + '</p>';
    html += '</div>';
  }

  if (data.analysis?.suggestions) {
    html += '<h2>Recommendations</h2>';
    data.analysis.suggestions.forEach((s: string) => {
      html += '<div class="checklist-item">' + s + '</div>';
    });
  }

  if (data.analysis?.kpiTargets) {
    html += '<h2>Next Period Targets</h2>';
    Object.entries(data.analysis.kpiTargets).forEach(([k, v]) => {
      html += '<div class="field-row"><span class="field-label">' + k.replace(/([A-Z])/g, ' $1') + '</span><span class="field-value">' + v + '</span></div>';
    });
  }

  downloadPDF(html, 'NH_SMM_Report_' + data.platform + '_' + data.period.replace(/\s/g, '_'));
}